#include "main.h"
#include <stdio.h>
/**
 * print_array - prints a number of elements of int array
 * @a: array of int being printed
 * @n: number of elements
 */
void print_array(int *a, int n)
{
	int i;

	for (i = 0; i < n; i++)
	{
		if (i == n - 1)
		{
			printf("%d\n", a[i]);
		}
		else
		{
			printf("%d, ", a[i]);
		}
	}
}
